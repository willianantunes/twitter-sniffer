package br.com.willianantunes.test.entity;

import java.io.Serializable;
import java.time.LocalDateTime;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;

@Entity
@NamedQueries({
	@NamedQuery(name="SELECT-ALL", query="SELECT t FROM TwitterMessage t"),
	@NamedQuery(name="DELETE-ALL", query="DELETE FROM TwitterMessage")
})
public class TwitterMessage implements Serializable {
	@Id
	@GeneratedValue
	private Integer id;
	@Column
	private String userName;
	@Column
	private String screenName;
	@Column
	private LocalDateTime createdAt;
	@Column
	private String text;
	
	public TwitterMessage() {}
	
	public TwitterMessage(String userName, String screenName, LocalDateTime createdAt, String text) {
		this.userName = userName;
		this.screenName = screenName;
		this.createdAt = createdAt;
		this.text = text;
	}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getScreenName() {
		return screenName;
	}
	public void setScreenName(String screenName) {
		this.screenName = screenName;
	}
	public LocalDateTime getCreatedAt() {
		return createdAt;
	}
	public void setCreatedAt(LocalDateTime createdAt) {
		this.createdAt = createdAt;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	@Override
	public String toString() {
		return "TwitterMessages [id=" + id + ", userName=" + userName + ", screenName=" + screenName + ", createdAt="
				+ createdAt + ", text=" + text + "]";
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((userName == null) ? 0 : userName.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TwitterMessage other = (TwitterMessage) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (userName == null) {
			if (other.userName != null)
				return false;
		} else if (!userName.equals(other.userName))
			return false;
		return true;
	}
}